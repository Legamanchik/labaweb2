//Функція пошуку найбільшого спільного дільника
function NSD(a, b)
{
    if (b > 0)
    {
        let k = a % b;
        return NSD(b, k);
    }
    else
        return a < 0 ? -a : a;

}

console.log("Найбільший спільний дільник 152 і 46 = " + NSD(152, 46));
console.log("Найбільший спільний дільник 31 і 62 = " + NSD(31, 62));
console.log("Найбільший спільний дільник 18 і 9 = " + NSD(18, 9));